package effects

import "time"

var effects = map[int][]rune{
	0: {'|', '/', '-', '\\'},
	1: {'⠋', '⠙', '⠹', '⠸', '⠼', '⠴', '⠦', '⠧', '⠇', '⠏'},
	2: {'◐', '◓', '◑', '◒'},
	3: {'←', '↖', '↑', '↗', '→', '↘', '↓', '↙'},
	4: {'▁', '▂', '▃', '▄', '▅', '▆', '▇', '█', '▇', '▆', '▅', '▄', '▃', '▂'},
	5: {'⡀', '⡄', '⡆', '⡇', '⣇', '⣧', '⣷', '⣿', '⣷', '⣧', '⣇', '⡇', '⡆', '⡄'},
	6: {'.', 'o', 'O', 'o'},
	7: {'◜', '◠', '◝', '◞', '◡', '◟'},
	8: {'▖', '▘', '▝', '▗'},
	9: {'□', '■'},
}

func Spinner(effect int, duration int64) rune {
	e := effects[effect]
	frame := int((time.Now().UnixMilli() / duration) % int64(len(e)))
	return e[frame]
}
